  <h1>Effect of Crime on your society:</h1>
  <hr>
  <p>Crime can cause significant social and economic problems to individuals and communities. Tackling crime can be expensive and can stretch budgets.
Crime not only affects economic productivity when victims miss work, but communities also are affected through loss of tourism and retail sales. Even the so-called victimless crimes of prostitution, drug abuse, and gambling have major social consequences. Drug abuse affects worker productivity, uses public funds for drug treatment programs and medical attention, and leads to criminal activity to support the expenses of a drug habit.
</p>
  <ul>
    <li>People may become frightened inside or outside their own home.</li>
    <li>Home insurance premiums can rise.</li>
    <li>Homeowners can find it more difficult to sell their property.
</li>
    <li>New businesses may avoid the area.</li>
    <li>Existing businesses may close down due to:</li>
   <ul>
     <li>Repeated thefts and loss of income</li>
     <li>Costs of repairing damage from vandalism</li>
     <li>Loss of customers in the area</li>
   </ul>
    <br>
  </ul>
</div>
