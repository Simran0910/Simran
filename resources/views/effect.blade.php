  <h1>What are the effects of crime on victims and thier relatives?</h1>
  <hr>
  <p>A victim of a crime may possibly experience many different kinds of effects:</p>
  <ul>
    <li>Direct costs and inconvenience due to theft of or damage to property (including time off work).</li>
    <li>The physical effects of injury through violent crime.</li>
    <li>Guilt at having become the victim of crime and feelings one could have prevented it (whether or not this was at all possible).
</li>
    <li>Psychological effects such as anger, depression or fear, which, in serious cases, can cause sleeplessness, flashbacks to the offence or Post-Traumatic Stress Disorder (PTSD).</li>
    <li>Feelings of anxiety through shock that such a thing has happened and worries about revictimisation, sometimes leading to feelings of loss of trust in one’s community and in society.</li>
    <li>Dealing with insurance claims and, for those for whom the crime is reported to the police, the police and other parts of the criminal justice system.</li>
    <br>
  </ul>
</div>
