  <h1>
Effect of Crime on an individual:
</h1>
  <hr>
  <p>Everyone is different and will therefore crime will affect them in different ways. The main thing is to understand that any changes in how you feel could be a result of the traumatic experience you’ve been through. 

The effects of crime can also last for a long time, and it doesn’t depend on how ‘serious’ the crime was. Some people cope really well with the most horrific crimes while others can be very distressed by a more minor incident:</p>
  <ul>
    <li>You feel angry, upset or experience other strong emotions.</li>
    <li>Things suddenly fall apart for you.</li>
    <li>You might have physical symptoms, such as lack of sleep or feeling ill.</li>
    <li>You blame yourself thinking you should have done things differently.</li>
    <li>You develop long-term problems such as depression or anxiety-related illness.</li>
    
    <br>
  </ul>
