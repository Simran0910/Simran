      <h1>What is Crime Prevention</h1>
      <hr>
  <p >Crime Prevention is the anticipation, recognition, and appraisal of a crime risk, and the initiation of action to remove or reduce it.

Crime Prevention is an active approach utilizing public awareness and preventive measures to reduce crime.<br>

Crime Prevention reflects a philosophy of self-defense where the police and the community take action before crimes are committed.

Crime Preventions programs will reduce crime, stimulate public awareness concerning crime prevention and enhance our communities.<br>

Crime prevention works. It is cheaper, safer, and healthier for communities to prevent crime than to have to treat its victims, deal with its perpetrators, and lose civic health and productivity.
   
  </p>

